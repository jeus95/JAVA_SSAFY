package com.singleton;

public class Book {
	private int isbn;
	private String title;
	private String author;
	private String publisher;
	private int Price;
	private int desc;


	
	
	
	
	
	public String toString() {
		return "Book [isbn=" + isbn + ", title=" + title + ", author=" + author + ", publisher=" + publisher
				+ ", Price=" + Price + ", desc=" + desc + "]";
	}
	
	
	
}
